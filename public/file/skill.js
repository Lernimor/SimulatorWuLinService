var searchSkill = function(id){
    for (let key in window.skills){
        if (key == id){
            return window.skills[key];
        }
    }
    return null;
}

var pushToSkill = function(key, list){
    window.skills[key] = list;
}


var showSkill = function(){
    funDownload('let skills = '+ JSON.stringify(window.skills), 'kill.js');    
}

var funDownload = function (content, filename) {
    var eleLink = document.createElement('a');
    eleLink.download = filename;
    eleLink.style.display = 'none';
    // 字符内容转变成blob地址
    var blob = new Blob([content]);
    eleLink.href = URL.createObjectURL(blob);
    // 触发点击
    document.body.appendChild(eleLink);
    eleLink.click();
    // 然后移除
    document.body.removeChild(eleLink);
};
